"""
Polygonal (Figurate) Number Functions

This module provides functions for working with different types of polygonal (figurate) numbers,
including calculating the nth number in each sequence, validating if a number belongs to a sequence,
and finding the closest polygonal number to a given value.

Mathematical Background:

1. Triangle Numbers: Represent the number of dots that can form an equilateral triangle.
   - Formula: n(n+1)/2
   - Sequence: 1, 3, 6, 10, 15, 21, 28, ...
   - Inverse: n is triangular if (-1 + sqrt(1 + 8n))/2 is an integer

2. Square Numbers: Represent the number of dots that form a square.
   - Formula: n²
   - Sequence: 1, 4, 9, 16, 25, 36, 49, ...
   - Inverse: n is a square if sqrt(n) is an integer

3. Pentagonal Numbers: Represent the number of dots that form a regular pentagon.
   - Formula: n(3n-1)/2
   - Sequence: 1, 5, 12, 22, 35, 51, 70, ...
   - Inverse: n is pentagonal if (1 + sqrt(1 + 24n))/6 is an integer

4. Hexagonal Numbers: Represent the number of dots that form a regular hexagon.
   - Formula: n(2n-1)
   - Sequence: 1, 6, 15, 28, 45, 66, 91, ...
   - Inverse: n is hexagonal if (1 + sqrt(1 + 8n))/4 is an integer

5. Heptagonal Numbers: Represent the number of dots that form a regular heptagon.
   - Formula: n(5n-3)/2
   - Sequence: 1, 7, 18, 34, 55, 81, 112, ...
   - Inverse: n is heptagonal if (3 + sqrt(9 + 40n))/10 is an integer

6. Octagonal Numbers: Represent the number of dots that form a regular octagon.
   - Formula: n(3n-2)
   - Sequence: 1, 8, 21, 40, 65, 96, 133, ...
   - Inverse: n is octagonal if (2 + sqrt(4 + 12n))/6 is an integer

For each figurate number type, this module provides three utils functions:
- nth_*_number: Calculate the nth number in the sequence (e.g., nth_triangle_number)
- is_*_number: Check if a number belongs to the sequence (e.g., is_square_number)
- closest_*_number: Find the closest numbers in the sequence to a given value (e.g., closest_pentagonal_number)

Additionally, the module provides:
- A FigurateNumber enum for type-safe references to different figurate number types
- Dictionary mappings (p_funcs, v_funcs, c_funcs) from figurate number types to their respective functions
- A p_gen generator function to efficiently generate figurate numbers within a range
- A comprehensive test suite in the main() function

All functions that require positive integer inputs include validation through the n_is_positive_integer decorator.
"""
