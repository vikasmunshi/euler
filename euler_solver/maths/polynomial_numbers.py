class FigurateNumber(Enum):
    """Enumeration of all figurate (polygonal) numbers.

    Each enum value represents a specific polygonal number type,
    with the value indicating the number of sides in the polygon.
    For example, TRIANGLE=3 for 3-sided polygons, SQUARE=4 for 4-sided, etc.

    This enum provides a type-safe way to reference different figurate number types
    and is used as keys in the function mapping dictionaries (p_funcs, v_funcs, c_funcs).
    """
    TRIANGLE = 3
    SQUARE = 4
    PENTAGONAL = 5
    HEXAGONAL = 6
    HEPTAGONAL = 7
    OCTAGONAL = 8

    def __str__(self) -> str:
        """Return the enum member as a title-cased string."""
        return self.name.title()


# Dictionary mapping figurate number types to their nth calculation functions
p_funcs: Dict[FigurateNumber, Callable[[int], int]] = {
    FigurateNumber.TRIANGLE: nth_triangle_number,
    FigurateNumber.SQUARE: nth_square_number,
    FigurateNumber.PENTAGONAL: nth_pentagonal_number,
    FigurateNumber.HEXAGONAL: nth_hexagonal_number,
    FigurateNumber.HEPTAGONAL: nth_heptagonal_number,
    FigurateNumber.OCTAGONAL: nth_octagonal_number,
}
# Dictionary mapping figurate number types to their validation functions
v_funcs: Dict[FigurateNumber, Callable[[int], bool]] = {
    FigurateNumber.TRIANGLE: is_triangle_number,
    FigurateNumber.SQUARE: is_square_number,
    FigurateNumber.PENTAGONAL: is_pentagonal_number,
    FigurateNumber.HEXAGONAL: is_hexagonal_number,
    FigurateNumber.HEPTAGONAL: is_heptagonal_number,
    FigurateNumber.OCTAGONAL: is_octagonal_number,
}
# Dictionary mapping figurate number types to their closest-finding functions
c_funcs: Dict[FigurateNumber, Callable[[int], Tuple[float, int, int]]] = {
    FigurateNumber.TRIANGLE: closest_triangle_number,
    FigurateNumber.SQUARE: closest_square_number,
    FigurateNumber.PENTAGONAL: closest_pentagonal_number,
    FigurateNumber.HEXAGONAL: closest_hexagonal_number,
    FigurateNumber.HEPTAGONAL: closest_heptagonal_number,
    FigurateNumber.OCTAGONAL: closest_octagonal_number,
}


def p_gen(p_num: FigurateNumber, min_value: int = 0, max_value: int = 2 ** 32) -> Generator[int, None, None]:
    """Generate polynomial numbers of a specific type within a range.

    Efficiently generates polynomial numbers by using the closest_*_number functions
    to determine the appropriate range of indices to check.

    Args:
        p_num: Type of polynomial (3=triangle, 4=square, 5=pentagonal, etc.)
        min_value: Minimum value to include in the sequence
        max_value: Maximum value to include in the sequence

    Yields:
        Each polynomial number within the specified range
    """
    p_func: Callable[[int], int] = p_funcs[p_num]
    c_func: Callable[[int], Tuple[float, int, int]] = c_funcs[p_num]
    start_num: int = floor(c_func(min_value)[0])
    stop_num: int = ceil(c_func(max_value)[0]) + 1
    for i in range(start_num, stop_num):
        if min_value <= (p := p_func(i)) <= max_value:
            yield p


def main() -> int:
    """Test and validate the figurate (polygonal) number calculations.

    This comprehensive validation function tests all aspects of the figurate number implementation:

    1. Calculating numbers using nth_*_number functions and verifying their correctness
    2. Validating numbers with is_*_number functions to ensure they properly identify figurate numbers
    3. Checking that integers between consecutive figurate numbers aren't incorrectly
       identified as figurate numbers (false positives)
    4. Testing the closest_*_number functions for accuracy with non-figurate numbers
    5. Verifying the p_gen generator function produces numbers within specified ranges

    The function outputs detailed diagnostic information during testing and
    maintains a count of failures encountered.

    Returns:
        int: The total number of validation failures encountered (0 if all tests pass)
    """
    failed: int = 0
    previous_p_number: int = 0
    for p_num, p_func, v_func, c_func in (
            (FigurateNumber.TRIANGLE, nth_triangle_number, is_triangle_number, closest_triangle_number),
            (FigurateNumber.SQUARE, nth_square_number, is_square_number, closest_square_number),
            (FigurateNumber.PENTAGONAL, nth_pentagonal_number, is_pentagonal_number, closest_pentagonal_number),
            (FigurateNumber.HEXAGONAL, nth_hexagonal_number, is_hexagonal_number, closest_hexagonal_number),
            (FigurateNumber.HEPTAGONAL, nth_heptagonal_number, is_heptagonal_number, closest_heptagonal_number),
            (FigurateNumber.OCTAGONAL, nth_octagonal_number, is_octagonal_number, closest_octagonal_number),
    ):
        for n in range(1, 121):
            p_number = p_func(n)
            if not (validated := v_func(p_number)):
                logger.error(f'calculated {p_func.__name__}({n})={p_number}, does not validate.')
                failed += 1
            logger.info(f'{p_func.__name__}({n})={p_number}; {v_func.__name__}({p_number})={validated}')
            if previous_p_number != 0:
                for not_a_p_number in range(previous_p_number + 1, p_number):
                    if v_func(not_a_p_number):
                        logger.error(f'{not_a_p_number=} also validates as {v_func.__name__}.')
                        failed += 1
                    i, previous_p, current_p = c_func(not_a_p_number)
                    if i > n or previous_p != previous_p_number or current_p != p_number:
                        logger.error(f'{c_func.__name__}({not_a_p_number})=({i:.2f}, {previous_p}, {current_p})')
                        failed += 1
            previous_p_number = p_number
        min_value: int = 1000
        max_value: int = 10000
        vals = list(p_gen(p_num, min_value=min_value, max_value=max_value))
        logger.info(f'{p_num} numbers: {vals}')
        if vals[0] < min_value or vals[-1] > max_value:
            logger.error(f'p_nums out of range: {vals}')
            failed += 1
    return failed


if __name__ == '__main__':
    """Entry point for figurate (polygonal) number validation script.

    When this module is run directly, it executes the comprehensive validation tests
    in the main() function and reports the results. The script exits with a status code
    equal to the number of test failures (0 indicates complete success).

    This allows the module to be used both as a library and as a standalone validation tool.
    """
    logger.setLevel('ERROR')
    print('Validating polynomial number calculations...')
    if (result := main()) != 0:
        print('Validation failed: Some polynomial number tests did not pass.')
    else:
        print('Validation passed: All polynomial number tests passed.')
    raise SystemExit(result)
