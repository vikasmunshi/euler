class NumberError(ValueError):
    """Exception raised when an invalid number is encountered."""
    pass


def n_is_positive_integer[T](func: Callable[[int], T]) -> Callable[[int], T]:
    """Decorator to ensure 'n' parameter is a positive integer.

    Raises AssertionError if n is not a positive integer.
    """

    @wraps(func)
    def wrapper(n: int) -> T:
        if isinstance(n, int) and n > 0:
            return func(n)
        else:
            raise NumberError(f"'n' must be a positive integer, got {n}")

    return wrapper


@n_is_positive_integer
def nth_triangle_number(n: int) -> int:
    """Calculate the nth triangle number.

    Formula: n * (n + 1) / 2

    Args:
        n: A positive integer

    Returns:
        The nth triangle number
    """
    return (n * (n + 1)) // 2


@n_is_positive_integer
def is_triangle_number(n: int) -> bool:
    """Check if a given number is a triangle number.

    Uses the formula: x = (-1 + sqrt(1 + 8n))/2
    If x is an integer, n is a triangle number.

    Args:
        n: A positive integer to check

    Returns:
        True if n is a triangle number, False otherwise
    """
    return ((-1 + sqrt(1 + 8 * n)) / 2).is_integer()


@n_is_positive_integer
def closest_triangle_number(n: int) -> Tuple[float, int, int]:
    """Find the closest triangle numbers to a given value.

    Uses the inverse formula: x = (-1 + sqrt(1 + 8n))/2
    to find the index that would generate the given number if it were triangular,
    then returns that index and the triangle numbers generated by its floor and ceiling.

    Args:
        n: A positive integer to find closest triangle numbers to

    Returns:
        A tuple containing:
        - The float index that would generate n if it were a triangle number
        - The largest triangle number less than or equal to n
        - The smallest triangle number greater than or equal to n
    """
    return (i := (-1 + sqrt(1 + 8 * n)) / 2), nth_triangle_number(floor(i)), nth_triangle_number(ceil(i))


